    public void adicionar() {
        String sql = "insert into usuario(nome, idade,sexo) values(?,?,?)";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtNome.getText());
            pst.setString(2, txtIdade.getText());
            pst.setString(3, cboSexo.getSelectedItem().toString());

            if (txtNome.getText().isEmpty() || txtIdade.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Preencha os campos obrigatorios");
            } else {
                int adicionado = pst.executeUpdate();
                if (adicionado > 0) {
                    JOptionPane.showMessageDialog(null, "Adicionado com sucesso");
                    limparCampos();
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

    public void pesquisar() {
        String sql = "select * from usuario where id=?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtId.getText());
            rs = pst.executeQuery();
            if (rs.next()) {
                txtNome.setText(rs.getString(2));
                txtIdade.setText(rs.getString(4));
                cboSexo.setSelectedItem(rs.getString(3));
            } else {
                JOptionPane.showMessageDialog(null, "Usu치rio n칚o encontrado");
                limparCampos();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    public void alterar() {
        String sql = "update usuario set nome=?, idade=?, sexo=? where id=?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtNome.getText());
            pst.setString(2, txtIdade.getText());
            pst.setString(3, cboSexo.getSelectedItem().toString());
            pst.setString(4, txtId.getText());
            if (txtNome.getText().isEmpty() || txtIdade.getText().isEmpty() || txtId.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Preencha os campos obrigatorios");
            } else {
                int alterado = pst.executeUpdate();
                if (alterado > 0) {
                    JOptionPane.showMessageDialog(null, "Dados alterados com sucesso");
                    limparCampos();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    public void deletar() {
        if (txtId.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha os campos ID");
        } else {
            int confirmar = JOptionPane.showConfirmDialog(null, "Deseja deletar esse/a usu치rio/a?", "ATEN칂츾O", JOptionPane.YES_NO_OPTION);
            if (confirmar == JOptionPane.YES_OPTION) {
                String sql = "delete from usuario where id=?";
                try {
                    pst = conexao.prepareStatement(sql);
                    pst.setString(1, txtId.getText());
                    int deletado = pst.executeUpdate();
                    if (deletado > 0) {
                        JOptionPane.showMessageDialog(null, "Usu치rio deletado com sucesso");
                        limparCampos();
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        }
    }

    public void limparCampos() {
        txtNome.setText("");
        txtIdade.setText("");
        txtId.setText("");
    }

